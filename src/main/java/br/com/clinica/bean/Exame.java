package br.com.clinica.bean;

public class Exame {
	private String exameOcupacional;
	private String exameComplementares;
	private String statusRestricao;
	
	public String getExameOcupacional() {
		return exameOcupacional;
	}
	public void setExameOcupacional(String exameOcupacional) {
		this.exameOcupacional = exameOcupacional;
	}
	public String getExameComplementares() {
		return exameComplementares;
	}
	public void setExameComplementares(String exameComplementares) {
		this.exameComplementares = exameComplementares;
	}
	public String getStatusRestricao() {
		return statusRestricao;
	}
	public void setStatusRestricao(String statusRestricao) {
		this.statusRestricao = statusRestricao;
	}
	
	
}
